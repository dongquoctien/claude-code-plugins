#!/usr/bin/env node
// Clone presentational components from a dev export into the prototype theme dir,
// rewriting imports that won't resolve in a static prototype.
//
// Quarantine rule: a component is moved to _quarantine/ if it imports anything we
// can't safely strip or rewrite (e.g., business logic from "@/lib/api/...").

import fs from 'node:fs';
import path from 'node:path';

function parseArgs(argv) {
  const out = {};
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--in') out.in = argv[++i];
    else if (a === '--out') out.out = argv[++i];
    else if (a === '--manifest') out.manifest = argv[++i];
  }
  if (!out.in || !out.out) {
    console.error('Usage: clone-components.mjs --in <components-dir> --out <output-dir> [--manifest <manifest.json>]');
    process.exit(2);
  }
  return out;
}

// Whitelist of "safe" external imports we leave untouched.
const SAFE_EXTERNAL = new Set([
  'react', 'react-dom',
  'clsx', 'classnames', 'tailwind-merge',
  'lucide-react',
  'date-fns', 'dayjs',
]);

// Imports we replace.
const REPLACEMENTS = [
  // next/* — not available in plain React/HTML prototypes
  { match: /^next\/image$/, action: 'drop', stripAll: true },
  { match: /^next\/link$/,  action: 'drop', stripAll: true },
  { match: /^next\/.+$/,    action: 'drop', stripAll: true },
  // @/lib/utils — usually `cn` helper. Replace with a local stub.
  { match: /^@\/lib\/utils$/, action: 'rewrite', to: './_utils' },
];

// Imports we CANNOT rewrite — quarantine the whole component.
const QUARANTINE_IF = [
  /^@\/lib\/api/,
  /^@\/hooks/,
  /^@\/store/,
  /^@\/server/,
  /^\/api\//,
];

const UTILS_STUB = `// Auto-generated by clone-components.mjs.
// Replaces @/lib/utils for the prototype.
import { clsx } from 'clsx';
import { twMerge } from 'tailwind-merge';
export function cn(...inputs) { return twMerge(clsx(inputs)); }
`;

const IMPORT_RE = /^\s*import\s+(?:[^'"]+from\s+)?['"]([^'"]+)['"]\s*;?\s*$/gm;

function classifyImport(spec) {
  for (const q of QUARANTINE_IF) if (q.test(spec)) return { kind: 'quarantine', reason: `imports ${spec}` };
  for (const r of REPLACEMENTS) if (r.match.test(spec)) return { kind: r.action, to: r.to, stripAll: r.stripAll };
  if (spec.startsWith('.') || spec.startsWith('/')) return { kind: 'keep' };
  if (SAFE_EXTERNAL.has(spec) || /^[a-z][a-z0-9-]+$/i.test(spec)) return { kind: 'keep' };
  if (spec.startsWith('@/')) return { kind: 'quarantine', reason: `unresolved alias ${spec}` };
  return { kind: 'keep' };
}

function rewriteSource(source) {
  let needsUtilsStub = false;
  const quarantineReasons = [];

  // Step 1: scan for import lines and decide per-line action.
  const lines = source.split(/\r?\n/);
  const out = [];
  for (const line of lines) {
    const m = /^\s*import\s+(?:([^'"]+?)\s+from\s+)?['"]([^'"]+)['"]\s*;?\s*$/.exec(line);
    if (!m) { out.push(line); continue; }
    const spec = m[2];
    const cls = classifyImport(spec);
    if (cls.kind === 'quarantine') { quarantineReasons.push(cls.reason); out.push(line); continue; }
    if (cls.kind === 'keep') { out.push(line); continue; }
    if (cls.kind === 'drop') {
      // remove import; leave a marker comment
      out.push(`// removed import: ${spec}`);
      continue;
    }
    if (cls.kind === 'rewrite') {
      if (cls.to === './_utils') needsUtilsStub = true;
      out.push(line.replace(spec, cls.to));
      continue;
    }
    out.push(line);
  }

  // Step 2: drop usages of removed default exports we can confidently strip.
  let body = out.join('\n');
  // next/image: <Image ... /> → <img ... />
  body = body.replace(/<Image\b/g, '<img');
  body = body.replace(/<\/Image>/g, '</img>');
  // next/link: <Link href="X"> → <a href="X">
  body = body.replace(/<Link\b/g, '<a');
  body = body.replace(/<\/Link>/g, '</a>');

  return { body, needsUtilsStub, quarantineReasons };
}

function main() {
  const { in: inDir, out: outDir, manifest: manifestPath } = parseArgs(process.argv);
  if (!fs.existsSync(inDir)) {
    console.error('input components dir not found:', inDir);
    process.exit(1);
  }
  fs.mkdirSync(outDir, { recursive: true });
  const quarantineDir = path.join(outDir, '_quarantine');

  let manifest = {};
  if (manifestPath && fs.existsSync(manifestPath)) {
    try { manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8')); } catch {}
  }
  const declaredComponents = manifest.components || [];

  const cloned = [];
  const quarantined = [];
  const ignored = [];
  let needsUtilsStub = false;
  const quarantineReasons = {};

  const entries = fs.readdirSync(inDir, { withFileTypes: true });
  for (const ent of entries) {
    if (!ent.isFile()) continue;
    const ext = path.extname(ent.name).toLowerCase();
    if (!['.tsx', '.jsx', '.ts', '.js', '.vue', '.svelte'].includes(ext)) {
      ignored.push(ent.name);
      continue;
    }
    const srcPath = path.join(inDir, ent.name);
    const src = fs.readFileSync(srcPath, 'utf8');
    const { body, needsUtilsStub: needs, quarantineReasons: reasons } = rewriteSource(src);

    if (reasons.length > 0) {
      fs.mkdirSync(quarantineDir, { recursive: true });
      fs.writeFileSync(path.join(quarantineDir, ent.name), src, 'utf8');
      quarantined.push(ent.name);
      quarantineReasons[ent.name] = reasons;
      continue;
    }
    fs.writeFileSync(path.join(outDir, ent.name), body, 'utf8');
    cloned.push(ent.name);
    if (needs) needsUtilsStub = true;
  }

  if (needsUtilsStub) {
    // Pick extension matching the cloned files
    const tsxLike = cloned.some(n => n.endsWith('.tsx') || n.endsWith('.ts'));
    const ext = tsxLike ? '.ts' : '.js';
    fs.writeFileSync(path.join(outDir, '_utils' + ext), UTILS_STUB, 'utf8');
  }

  // _manifest.json — successfully cloned components + props mirrored from input manifest
  const compMeta = cloned.map(file => {
    const base = path.basename(file, path.extname(file));
    const declared = declaredComponents.find(c => c.name === base || c.path?.endsWith('/' + file));
    return { name: base, file, props: declared?.props || [], variants: declared?.variants || [] };
  });
  fs.writeFileSync(path.join(outDir, '_manifest.json'), JSON.stringify({ components: compMeta }, null, 2) + '\n', 'utf8');

  if (quarantined.length > 0) {
    fs.writeFileSync(path.join(quarantineDir, '_reasons.json'), JSON.stringify(quarantineReasons, null, 2) + '\n', 'utf8');
  }

  console.log(JSON.stringify({ cloned, quarantined, ignored, quarantineReasons, utilsStubCreated: needsUtilsStub }, null, 2));
}

main();
